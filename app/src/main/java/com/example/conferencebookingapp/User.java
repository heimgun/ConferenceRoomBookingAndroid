package com.example.conferencebookingapp;

public class User {

    String firstName;
    String lastName;
    String username;
    String password;
    int phone;
    String organization;
    int orgNumber;
    String street;
    String city;
    int zipCode;



    public String getFirstName() {
        return firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public String getUsername() {
        return username;
    }

    public String getPassword() {
        return password;
    }

    public int getPhone() {
        return phone;
    }

    public String getOrganization() {
        return organization;
    }

    public int getOrgNumber() {
        return orgNumber;
    }

    public String getStreet() {
        return street;
    }

    public String getCity() {
        return city;
    }

    public int getZipCode() {
        return zipCode;
    }
}
